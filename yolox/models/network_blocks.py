class My_TransformerEncoderLayer(nn.Module):
    """Transformer Encoder."""

    def __init__(self, c1, cm=256, num_heads=8, dropout=0.0, act=nn.GELU(),num_parms=9):
        super().__init__()
        self.ma = nn.MultiheadAttention(c1, num_heads, dropout=dropout, batch_first=True)
        # Implementation of Feedforward model
        self.fc1 = nn.Linear(c1, cm)
        self.fc2 = nn.Linear(cm, c1)

        self.norm1 = nn.LayerNorm(c1)
        self.norm2 = nn.LayerNorm(c1)
        self.dropout = nn.Dropout(dropout)
        self.dropout1 = nn.Dropout(dropout)
        self.dropout2 = nn.Dropout(dropout)

        self.tr_param = nn.Parameter(torch.randn(1,num_parms, c1))
        self.act = act

    def forward_post(self, src):
    
        q =src
        v = k = self.tr_param
        src2 = self.ma(q, k, v)[0]
        src = src + self.dropout1(src2)
        src = self.norm1(src)
        src2 = self.fc2(self.dropout(self.act(self.fc1(src))))
        src = src + self.dropout2(src2)
        src = self.norm2(src)
        return src


    def forward(self, src):
        """Forward propagates the input through the encoder module."""
       
        return self.forward_post(src)
